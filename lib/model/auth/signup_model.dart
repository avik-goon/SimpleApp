class UserSignUp {
  String? _fullname;
  String? _mobileNumber;
  String? _email;
  String? _password;

  get geFullname => _fullname;

  set setFullname(String? value) => _fullname = value;

  get getMobileNumber => _mobileNumber;

  set setMobileNumber(String? value) => _mobileNumber = value;

  get getEmail => _email;

  set setEmail(String? value) => _email = value;

  get getPassword => _password;

  set setPassword(String? value) => _password = value;
}
